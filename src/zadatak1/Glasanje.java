package zadatak1;



public class Glasanje {
	
	public static String glasanje(int i) {

		String glas = "";
		switch (i) {
		case 1:
			glas = "Mijau";
			break;

		case 2:
			glas = "Vau-vau";
			break;

		case 3:
			glas = "Muuuuu";
			break;

		case 4:
			glas = "Kokodak";
			break;
		}

		return glas;

	}
}
