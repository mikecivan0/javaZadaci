package zadatak7_2_parametriziranIspisSpiralneMatrice;

public class Vrijednosti {
	
	private int redovi;	
	private int stupci;	
	private int smjer;
	private int pocetak;
	
	public int getRedovi() {
		return redovi;
	}
	public void setRedovi(int redovi) {
		this.redovi = redovi;
	}	
	public int getStupci() {
		return stupci;
	}
	public void setStupci(int stupci) {
		this.stupci = stupci;
	}
	public int getSmjer() {
		return smjer;
	}
	public void setSmjer(int smjer) {
		this.smjer = smjer;
	}
	public int getPocetak() {
		return pocetak;
	}
	public void setPocetak(int pocetak) {
		this.pocetak = pocetak;
	}

}
